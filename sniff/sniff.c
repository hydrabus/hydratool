//! Byte swap unsigned short
t_u16 swap_u16(t_u16 val )
{
	return (val << 8) | (val >> 8 );
}

//! Byte swap unsigned int
t_u32 swap_u32(t_u32 val )
{
	val = ((val << 8) & 0xFF00FF00 ) | ((val >> 8) & 0xFF00FF ); 
	return (val << 16) | (val >> 16);
}

/* Binary/hexadecimal(8bits) to ASCII Hexadecimal character(2*8bits) */
t_ascii_hex BinHextoAscii(t_u8 val)
{
	t_ascii_hex val_ascii_hex;
	t_u8 val_msb;
	t_u8 val_lsb;
	
	val_msb = val>>4;
	val_lsb = val&0x0F;
	
	/* Convert Hex to Ascii for MSB */
	if(val_msb <= 9)
	{
		val_ascii_hex.msb = (val_msb + '0');
	}else /* between 0xA to 0xF */
	{
		val_ascii_hex.msb = (val_msb + 'A' - 10);
	}
	/* Convert Hex to Ascii for LSB */
	if(val_lsb <= 9)
	{
		val_ascii_hex.lsb = (val_lsb + '0');
	}else /* between 0xA to 0xF */
	{
		val_ascii_hex.lsb = (val_lsb + 'A' - 10);
	}
	
	return(val_ascii_hex);
}

t_sniff_frame_state sniff_frame_header_decode(const t_u8 in_len_u8[SNIFF_FRAME_HEADER_DECODE_NB_BYTES], t_sniff_frame* in_out_frame)
{
    t_sniff_frame_state frame_state;

    memcpy(&in_out_frame->data_hdr[in_out_frame->data_hdr_size], &in_len_u8[0], SNIFF_FRAME_HEADER_DECODE_NB_BYTES);
    in_out_frame->data_hdr_size = SNIFF_FRAME_HEADER_DECODE_NB_BYTES;

    /* Read protocol_options */
    memcpy(&in_out_frame->protocol_options, &in_len_u8[0], SNIFF_FRAME_PROTOCOL_OPTIONS_NB_BYTES);

    /* Read protocol_modulation */
    memcpy(&in_out_frame->protocol_modulation, &in_len_u8[1], SNIFF_FRAME_PROTOCOL_MODULATION_NB_BYTES);

    /* Read data_size 16bits */
    memcpy(&in_out_frame->data_size, &in_len_u8[2], SNIFF_FRAME_DATA_SIZE_NB_BYTES);

	/* Error corrupted frame */
    if( (in_out_frame->data_size < SNIFF_FRAME_HEADER_DECODE_NB_BYTES) ||
        (in_out_frame->data_size >= 255)
		)
	{
        /* Error resync => init */
		return SNIFF_FRAME_INIT_DECODE;
	}

    /* Removed Header Size from Data size */
    in_out_frame->data_size = in_out_frame->data_size - SNIFF_FRAME_HEADER_DECODE_NB_BYTES;

    if(in_out_frame->protocol_options & PROTOCOL_OPTIONS_START_OF_FRAME_TIMESTAMP)
    {
        /* Remove start timestamp size from data_size */
        in_out_frame->data_size = in_out_frame->data_size - SNIFF_FRAME_TIMESTAMP_DECODE_NB_BYTES;

        frame_state = SNIFF_FRAME_START_TIMESTAMP_DECODE;
    }else
    {
        /* Next step read & decode Data defined by out_frame->data_size */
        frame_state = SNIFF_FRAME_DATA_DECODE;
    }

    if(in_out_frame->protocol_options & PROTOCOL_OPTIONS_END_OF_FRAME_TIMESTAMP)
    {
        /* Remove end timestamp size from data_size */
        in_out_frame->data_size = in_out_frame->data_size - SNIFF_FRAME_TIMESTAMP_DECODE_NB_BYTES;
    }

    return frame_state;
}

/* in_timestamp_u8 => timestamp 32bits */
t_sniff_frame_state sniff_frame_timestamp_decode(const t_u8 in_timestamp_u8[SNIFF_FRAME_TIMESTAMP_DECODE_NB_BYTES], t_sniff_frame* in_out_frame, bool start_of_frame)
{
    t_sniff_frame_state frame_state;
	t_u32 timestamp;

    /* Read TimeStamp 32bits */
    memcpy(&timestamp, &in_timestamp_u8[0], SNIFF_FRAME_TIMESTAMP_DECODE_NB_BYTES);

    if(start_of_frame == true)
    {
        in_out_frame->start_of_frame_time.timestamp = timestamp;
        /* Next step read & decode Data defined by out_frame->data_size */
        frame_state = SNIFF_FRAME_DATA_DECODE;
    }else
    {
        in_out_frame->end_of_frame_time.timestamp = timestamp;
        frame_state = SNIFF_FRAME_INIT_DECODE;
    }

    return frame_state;
}

bool is_frame_contains_end_timestamp(const t_sniff_frame* in_frame)
{
    if(in_frame->protocol_options & PROTOCOL_OPTIONS_END_OF_FRAME_TIMESTAMP)
    {
        return true;
    }else
    {
        return false;
    }
}

